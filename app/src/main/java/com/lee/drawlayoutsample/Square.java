public class Square {

    private int xmin;
    private int ymin;
    private int xmax;
    private int ymax;
    private String typeofSquare;
    public void setXmin(int xmin) {
        this.xmin = xmin;
    }
    public int getXmin() {
        return xmin;
    }

    public void setYmin(int ymin) {
        this.ymin = ymin;
    }
    public int getYmin() {
        return ymin;
    }

    public void setXmax(int xmax) {
        this.xmax = xmax;
    }
    public int getXmax() {
        return xmax;
    }

    public void setYmax(int ymax) {
        this.ymax = ymax;
    }
    public int getYmax() {
        return ymax;
    }

    public void setTypeofSquare(String typeofSquare) {
        this.typeofSquare = typeofSquare;
    }
    public String getTypeofSquare() {
        return typeofSquare;
    }

}
